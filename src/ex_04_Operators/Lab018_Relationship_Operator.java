package ex_04_Operators;

public class Lab018_Relationship_Operator {

    public static void main(String[] args) {

        // < Less Than
        // > Greater Than
        // < = Less Than or equal to
        // > = Greater than or equal to
        //  = = Equal to
        // ! = Not Equal to

        // ALl of then result is boolean

        int dhrithi_age = 7;
        int vedhansh_age = 5;

        boolean result = dhrithi_age > vedhansh_age;
        boolean result1 = dhrithi_age < vedhansh_age;
        boolean result2 = dhrithi_age >= vedhansh_age;

        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);




    }
}
